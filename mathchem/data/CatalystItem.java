package mathchem.data;

public class CatalystItem {
	private Long id;
	private String name;
	private String method;
	private String ea;
	private String lnk0;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMethod() {
		return method;
	}
	public void setMethod(String method) {
		this.method = method;
	}
	public String getEa() {
		return ea;
	}
	public void setEa(String ea) {
		this.ea = ea;
	}
	public String getLnk0() {
		return lnk0;
	}
	public void setLnk0(String lnk0) {
		this.lnk0 = lnk0;
	}
	
}
