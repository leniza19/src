package mathchem.data;

public class ConstItem {
	private Long idCalc;
	private Long idExp;		
	private String constant;
	private String c0;
	private String tempr;
	
	public Long getIdCalc() {
		return idCalc;
	}
	public void setIdCalc(Long idCalc) {
		this.idCalc = idCalc;
	}
	
	public Long getIdExp() {
		return idExp;
	}
	public void setIdExp(Long idExp) {
		this.idExp = idExp;
	}
	
	public String getConstant() {
		return constant;
	}
	public void setConstant(String constant) {
		this.constant = constant;
	}
	public String getC0() {
		return c0;
	}
	public void setC0(String c0) {
		this.c0 = c0;
	}
	public String getTempr() {
		return tempr;
	}
	public void setTempr(String tempr) {
		this.tempr = tempr;
	}

	
}
